// ── factory ───────────────────────────────────────────────────────────────────

pub fn make_theme(name: ThemeName) -> Theme {
    match name {
        ThemeName::Dark      => dark_theme(),
        ThemeName::Light     => light_theme(),
        ThemeName::Solarized => solarized_theme(),
        ThemeName::Nord      => nord_theme(),
    }
}
