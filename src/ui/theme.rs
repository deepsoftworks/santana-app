use ratatui::style::Color;
use crate::config::ThemeName;

/// Per-stream color: a bullet color and gradient endpoints (RGB).
#[derive(Debug, Clone, Copy)]
pub struct StreamColor {
    pub bullet:     Color,
    pub grad_start: (u8, u8, u8),
    pub grad_end:   (u8, u8, u8),
}

#[derive(Debug, Clone)]
pub struct Theme {
    pub border:        Color,
    pub title:         Color,
    pub axis:          Color,
    pub separator:     Color,
    pub status_header: Color,
    pub help_key:      Color,
    pub help_desc:     Color,
    pub selected_bg:   Color,
    pub eof_color:     Color,
    pub dim:           Color,
    pub streams:       Vec<StreamColor>, // 12 entries, cycling
}

pub fn gradient_color(start: (u8, u8, u8), end: (u8, u8, u8), t: f64) -> Color {
    let t = t.clamp(0.0, 1.0);
    let r = (start.0 as f64 + t * (end.0 as f64 - start.0 as f64)).round() as u8;
    let g = (start.1 as f64 + t * (end.1 as f64 - start.1 as f64)).round() as u8;
    let b = (start.2 as f64 + t * (end.2 as f64 - start.2 as f64)).round() as u8;
    Color::Rgb(r, g, b)
}

